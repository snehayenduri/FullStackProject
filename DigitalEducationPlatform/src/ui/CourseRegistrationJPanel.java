     private void populateTable(List<CourseOffer> courseOffers) {
        DefaultTableModel acctTbl = (DefaultTableModel) tableCourseOffers.getModel();
        acctTbl.setRowCount(0);
        for(CourseOffer courseOffer : courseOffers){
            Object[] row = new Object[7];
            row[0] = courseOffer;
            row[1] = courseOffer.getCourse().getCourseID();
            row[2] = String.valueOf(courseOffer.getCourse().getCredits());
            row[3] = String.valueOf(courseOffer.getSeatList().size());
            row[4] = courseOffer.getProfessor();
            row[5] = String.valueOf(courseOffer.getProfessor().getRating());
            row[6] = courseOffer.getTiming();
            acctTbl.addRow(row);
        }
    }
