    private void RegisterbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterbtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel acctTbl = (DefaultTableModel) tableCourseOffers.getModel();
        int selectedRow = tableCourseOffers.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this, "Please select a course");
            return;
        }
        CourseOffer registeringCourse = (CourseOffer) acctTbl.getValueAt(selectedRow,0);
        if(courseSchedule.getStudentCourses().contains(registeringCourse)){
            JOptionPane.showMessageDialog(this,"Already registered");
            return;
        }
        SeatAssignment newSeatAssignment = new SeatAssignment(registeringCourse, student);
        Seat newSeat = newSeatAssignment.assignSeat(registeringCourse, student);
        if(newSeat==null){
            JOptionPane.showMessageDialog(this, "The selected course is full!");
            return;
        }
        courseSchedule.getStudentCourses().add(registeringCourse);
    }//GEN-LAST:event_RegisterbtnActionPerformed

