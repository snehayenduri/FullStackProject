    private void refreshTable() {
        ArrayList<Channel> channelList = (ArrayList)business.getChannelCatalog().getChannelList();

        if (channelList.isEmpty()) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel)tblChannelCatalog.getModel();
        model.setRowCount(0);
        for (Channel channel : channelList) {
                Object[] row = new Object[1];
                row[0] = channel;
                model.addRow(row);
            }
        ArrayList<Market> marketList = business.getMarketCatalog().getMarkets();

        if (marketList.isEmpty()) {
            return;
        }
        DefaultTableModel model1 = (DefaultTableModel)tblMarketCatalog.getModel();
        model1.setRowCount(0);
        for (Market market : marketList) {
                Object[] row = new Object[1];
                row[0] = market;
                model1.addRow(row);
            }
        
        ArrayList<MarketChannelAssignment> mcaList = business.getMarketChannelComboCatalog().getMcalist();
        if (mcaList.isEmpty()) {
            return;
        }
        DefaultTableModel model2 = (DefaultTableModel)tblMarketChannel.getModel();
        model2.setRowCount(0);
        for (MarketChannelAssignment mca : mcaList) {
                Object[] row = new Object[2];
                row[0] = mca;
                row[1] = mca.getChannel();
                model2.addRow(row);
            }
        }
  
        
