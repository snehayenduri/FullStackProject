    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedMarketRow = tblMarketCatalog.getSelectedRow();
        if(selectedMarketRow< 0){
            JOptionPane.showMessageDialog(this,"Select a Market");
            return;
        }
        selectedMarket = (Market) tblMarketCatalog.getValueAt(selectedMarketRow, 0);
        
        int selectedChannelRow = tblChannelCatalog.getSelectedRow();
        if(selectedChannelRow<0){
            JOptionPane.showMessageDialog(this,"Select a channel");
            return;
        }
        selectedChannel = (Channel) tblChannelCatalog.getValueAt(selectedChannelRow, 0);
        marketChannelComboCatalog.newMarketChannelCombo(selectedMarket, selectedChannel);
        refreshTable();
    }//GEN-LAST:event_jButton1ActionPerformed

