    private void btnManageEventActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageEventActionPerformed
        // TODO add your handling code here:
       //int tableSize = tblHostedEvents.getRowCount();
       int selectedRow = tblHostedEvents.getSelectionModel().getLeadSelectionIndex();
       if(selectedRow<0){
           JOptionPane.showMessageDialog(this, "Please Select a Row");
           return;
       }
       Event selectedEvent = (Event) tblHostedEvents.getValueAt(selectedRow, 0);
       
       if(selectedEvent == null)
           return;
       if(selectedEvent.getEventManagement()!=null){
           JOptionPane.showMessageDialog(this,"Event Management Organization has already been Set!!");
           return;
       }
       
       ManageEventPanel manageEventPanel = new ManageEventPanel(workArea, organization, business, selectedEvent);
   
        workArea.add("ManageEventJPanel", manageEventPanel);
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.next(workArea);
       
       
    }//GEN-LAST:event_btnManageEventActionPerformed

