    public void initTable(){
        DefaultTableModel model = (DefaultTableModel) tblHostedEvents.getModel();
        model.setRowCount(0);
        
        for(Event event: organization.getHostedEvents()){
                Object[] row = new Object[5];
                row[0] = event;
                row[1] = event.getType();
                row[2] = event.getDate();
                row[3] = event.getTime();
                row[4] = event.getEventCapacity();
            
                model.addRow(row);
        }
    }

