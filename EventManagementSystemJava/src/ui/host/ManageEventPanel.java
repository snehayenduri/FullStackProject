    public void initTable(){
        DefaultTableModel model = (DefaultTableModel) tblEventOrganization.getModel();
        
        model.setRowCount(0);
        EventManagementOrganization mOrganization = null;
        for(Organization organization: business.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof EventManagementOrganization){
                mOrganization = (EventManagementOrganization) organization;
                Object[] row = new Object[2];
                row[0] = mOrganization;
                row[1] = mOrganization.getManagedEvents().size();
                
                model.addRow(row);
            }
        }
        
    }

