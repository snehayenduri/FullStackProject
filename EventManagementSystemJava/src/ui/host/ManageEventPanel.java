    private void btnDelegateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelegateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEventOrganization.getSelectionModel().getLeadSelectionIndex();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
            return;
        }
        EventManagementOrganization eventOrganization = (EventManagementOrganization) tblEventOrganization.getValueAt(selectedRow, 0);
        eventOrganization.getManagedEvents().add(event);
        event.setEventManagement(eventOrganization);
        event.setStatus(EventStatusEnum.Accepted);
        JOptionPane.showMessageDialog(this, "Assigned successfully");
        initTable();
    }//GEN-LAST:event_btnDelegateActionPerformed

