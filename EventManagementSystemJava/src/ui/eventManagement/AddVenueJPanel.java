    public void initTable(){
        DefaultTableModel model = (DefaultTableModel) tblVenue.getModel();
        model.setRowCount(0);
        
        for(Organization organization: business.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof VenueOrganization){
                VenueOrganization vOrganization = (VenueOrganization)organization;
                Object[] row = new Object[2];
                row[0] = vOrganization;
                row[1] = vOrganization.getAvailability();
                model.addRow(row);
            }
        }
    }

