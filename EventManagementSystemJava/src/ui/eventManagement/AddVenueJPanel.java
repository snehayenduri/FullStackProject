    private void btnVenueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenueActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVenue.getSelectionModel().getLeadSelectionIndex();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this,"Please select a venue");
            return;
        }
        VenueOrganization venue = (VenueOrganization) tblVenue.getValueAt(selectedRow,0);
        if(venue.getAvailability()==VenueOrganization.VenueAvailability.Unavailable){
            JOptionPane.showMessageDialog(this,"Please select an Available Venue!");
            return;
        }
        venue.getHostedEvents().add(event);
        event.setVenue(venue);
        JOptionPane.showMessageDialog(this, "Venue Added");
    }//GEN-LAST:event_btnVenueActionPerformed

