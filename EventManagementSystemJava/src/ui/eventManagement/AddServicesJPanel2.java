    private void jButton1AddProductItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1AddProductItemActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblService.getSelectionModel().getLeadSelectionIndex();

        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please Select a Product");
            return;
        }

        Product product = (Product) tblService.getValueAt(selectedRow, 0);
        event.getProductList().add(product);
        ServiceOrganization organization = (ServiceOrganization) ServiceComboBox.getSelectedItem();
        organization.getServicedEvents().add(event);
        
        JOptionPane.showMessageDialog(this, "Order Placed..");
    }//GEN-LAST:event_jButton1AddProductItemActionPerformed

